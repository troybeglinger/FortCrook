    <h3>Applying for Membership</h3>  
    <p>One of Masonry’s customs is not to solicit members. Therefore, you must seek membership by either asking a Mason or contacting a local lodge. All California men age 18 or older who are of excellent character and intention, and believe in a Supreme Being, may apply.</p>
    <p>The Mason who recommends you for membership will assist with completing and submitting the Application for Degrees. However, two Master Masons who are members of the Lodge must also recommend you. So, if you don’t know any, please contact the Secretary of the Lodge.</p>
    <p>We will read your completed application at the Lodge’s next Stated Meeting. If accepted, we will then appoint a committee to perform a confidential interview and answer any further questions you may have. After, we will bring your application to the lodge for a vote.</p>
    <p>There, the Master will announce the results of the investigating committee’s reports. Then we will cast a secret ballot, which must be unanimously favorable for you to proceed. The Secretary will notify you of the outcome either way, but if accepted, the Master will schedule your First Degree ceremony. Then, once you’ve advanced through the next two degrees, you will become a Master Mason and a full member of the fraternity.</p>
    <h3>Completing the Application for Degrees</h3>
    <strong>Page 1: Recommendations</strong>
    <p>The Mason or the lodge that provides the application handles completion of the first page. This page includes recommendations for membership by two Master Masons who attend the lodge that you wish to join, as well as two personal references.</p>
    <strong>Page 2: Residence History</strong>
    <p>Applicants must have been a California resident for the immediate 12 months preceding the date of the application.</p>
    <p>We grant exceptions for applicants who are members (or whose spouse is a member) of any uniformed service of the United States who has lived for at least six months at a duty station in California or has been on to a vessel with a home port in California.</p>
    <p>This exception also includes those who have received an honorable discharge or separation in the six months immediately preceding the date of the application. The applicant must also have been a California resident during the 24 months prior to their most recent induction or a California resident on the date of the application.</p>
    <p>Uniformed services include the Army and Army National Guard, Air Force, Navy, Marines, Coast Guard, Public Health Service, and the National Oceanic and Atmospheric Administration.</p>
    <strong>Page 2: Family Information</strong>
    <p>Freemasons have an obligation to care for fellow Masons, their wives, widows, and orphans. Lodge brothers are the first line of support. We also offer additional support and care through the Masonic Center for Youth and Families, statewide Masonic Outreach Services, and our Masonic Homes of California residential communities for members in good standing. Therefore, we request family information in order to ensure the appropriate fraternal support in case of need.</p>
    <strong>Page 3: Employment History</strong>
    <p>Information about your present and previous occupations allows us to get a better understanding of not only you, but your interests and skills. This also helps us to discern how you can best fit in and fulfill your obligations as a Mason.</p>
    <strong>Page 3: Masonic Affiliations</strong>
    <p>This section informs us about your familiarity with the fraternity. Please note, however, that we cannot consider an applicant rejected by another lodge within the preceding 12 months.</p>
    <strong>Page 3: Criminal History</strong>
    <p>An applicant’s character is the most important factor when being considered for membership. Men guilty of crimes contrary to community standards of justice, honesty, or good morals are not eligible for membership. This includes crimes such as forgery, rape, robbery, and solicitation by prostitutes.</p>
    <p>We conduct a criminal background check, including civil and criminal records, for each application for membership, including applications for affiliation from members of other jurisdictions.</p>
    <strong>Page 4: Fees and Contributions</strong>
    <p>Each lodge sets the fee for receiving the degrees of Masonry. We explain the fees on the application, and they are not refundable once we accept you for membership. They also apply to applicants for affiliation from another Masonic jurisdiction, but not to applicants for affiliation from another California lodge.</p>
    <p>Contributions go to support charitable programs provided by the Masonic Homes of California to help brothers and their families in times of need. They also help to maintain the George Washington Masonic Memorial in Washington, D.C. and the California Masonic Memorial Temple in San Francisco, along with its library and museum.</p>
    <strong>Page 4: Statement by the Applicant</strong>
    <p>Your signature affirms that you understand and meet the qualifications set forth in the application. It also means that you have read this information for applicants, and you agree to the required investigation and background checks.</p>
    <h3>Completing the Application for Affiliation</h3>
    <p>Masons moving to or within the state of California can apply to affiliate with any lodge recognized by the Grand Lodge of California.</p>
    <p>You may request an application from any Masons you know or lodges you’d like to join. They will complete the first page for you, including recommendations for membership by two Master Masons from the lodge, and two personal references.</p>
    <p>The same process of consideration from the application for degrees applies when transferring your membership from one lodge to another. However, Masons may also keep their current membership(s) if joining new lodges as a dual or plural member.</p>
